public class Calculation {
    public int add(int firstNumber, int secondNumber){
        int resultSum = firstNumber + secondNumber;
        return resultSum;
    }
    public int subtract(int firstNumber, int secondNumber){
        int resultDifference = firstNumber - secondNumber;
        return resultDifference;
    }
    public int multiply(int firstNumber, int secondNumber){
        int resultProduct = firstNumber * secondNumber;
        return resultProduct;
    }
    public float divide(float firstNumber, float secondNumber){
        float resultQuotient = firstNumber / secondNumber;
        return resultQuotient;
    }
}
